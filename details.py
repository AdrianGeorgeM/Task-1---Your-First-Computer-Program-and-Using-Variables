# Pseudocode:
# 1. Prompt the user to enter their name and store the input in a variable 'name'
name = input("What is your name? ")

# 2. Prompt the user to enter their age and store the input in a variable 'age'
age = input("What is your age? ")

# 3. Prompt the user to enter their house number and store the input in a variable 'house_number'
house_number = input("What is your house number? ")

# 4. Prompt the user to enter their street name and store the input in a variable 'street_name'
street_name = input("What is your street name? ")

# 5. Print a formatted string that includes the user's name, age, house number, and street name
print(f"This is {name}. He is {age} years old and lives at house number {house_number} on {street_name} street.")